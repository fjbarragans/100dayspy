alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']
import os
def caesar(direction_d, text_t, shift_s):
    cypher_text = ""
    if direction_d == "decode":
        shift_s *= -1
    for i in text_t:
        if i in alphabet:
            position = alphabet.index(i)
            new_position = (position + shift_s) % len(alphabet)
            new_letter = alphabet[new_position]
            cypher_text += new_letter
        else:
            cypher_text += i
    print(f"Here's the {direction_d}d result: {cypher_text}")

restart = True
