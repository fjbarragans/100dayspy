    direction = input("Type 'encode' to encrypt, type 'decode' to decrypt:\n")
    text = input("Type your message:\n").lower()
    shift = int(input("Type the shift number:\n"))
    caesar(direction_d=direction, text_t=text, shift_s=shift)
    decision = input("Do you want to restart the cipher program?\n")
    if decision == "no":
        restart = False